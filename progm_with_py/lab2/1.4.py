#Example of logical operators
age = 25
has_license = True

#Using 'and'
if age >= 18 and has_license:
    print("You can drive")

#Using 'or'
day = "Saturday"
if day == "Saturday" or day == "Sunday":
    print("Its weekend")

#Using 'not'
is_raining= False
if not is_raining:
    print("Good day for a walk")
